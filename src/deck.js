const decks = {
  Haber,
  Hacer,
  Estar,
  Ser,
  Ir,
  Nadar,
  Comer,
  Vivir
};
const verbs = ["Haber", "Hacer", "Estar", "Ser", "Ir", "Nadar", "Comer", "Tener", "Vivir"].sort();
const selectedVerbs = {};
verbs.forEach(verb => {
  selectedVerbs[verb] = true;
})
const tenses = ["Present", "Preterit", "Imperfect", "Future", "Conditional"];
const selectedTenses = {};
tenses.forEach(tense => {
  selectedTenses[tense] = true;
})

const deck = {
  decks,
  verbs,
  selectedVerbs,
  tenses,
  selectedTenses,

  verb: "",
  tense: "",
  subject: "",
  conjugation: "",

  shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
    }
  },
  shuffle() {
    this.shuffleArray(this.verbs);
    this.shuffleArray(this.tenses);
    this.verb = this.verbs[0];
    this.tense = this.tenses[0];

    let deck = this.decks[this.verb][this.tense];
    this.shuffleArray(deck);

    this.subject = deck[0].subject;
    this.conjugation = deck[0].conjugation;
  },
  buildVerbs() {
    this.verbs = [];
    for (var i in this.selectedVerbs) {
      if (this.selectedVerbs[i] === true) {
        this.verbs.push(i);
      }
    }
  },
  buildTenses() {
    this.tenses = [];
    for (var i in this.selectedTenses) {
      if (this.selectedTenses[i] === true) {
        this.tenses.push(i);
      }
    }
  },
  buildAndShuffle() {
    this.buildTenses();
    this.buildVerbs();

    if (this.verbs.length === 0) {
      alert("Error: You must select as least one verb.")
      return false;
    }

    if (this.tenses.length === 0) {
      alert("Error: You must select as least one tense.")
      return false;
    }

    this.shuffle();
    return true;
  }
};

export {
  deck,
  verbs,
  tenses
};